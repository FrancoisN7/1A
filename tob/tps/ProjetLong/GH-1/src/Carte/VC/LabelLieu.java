package Carte.VC;

import Carte.Modele.LieuCarte;

import javax.swing.JLabel;

public class LabelLieu extends JLabel {

    private final LieuCarte lieu;

    public LabelLieu(LieuCarte lieu){
        super(lieu.getImage());
        this.lieu = lieu;
    }

    public LieuCarte getLieu(){
        return lieu;
    }
}
